class Solution{
public:	
	
	
	int isPalindrome(string S)
	{
	    int len = S.length() - 1;
	    int st = 0;
	    while(st <= len){
	        if(S[st] != S[len]){
	            return 0;
	        }
	        st++;
	        len--;
	    }
	    return 1;
	    
	}

};

